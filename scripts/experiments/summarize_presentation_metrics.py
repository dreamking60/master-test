#!/usr/bin/env python3
"""Summarize the metrics used in the final presentation.

This script intentionally reads raw experiment logs instead of the presentation
Markdown, so the numbers in PPT_BUILD_PLAN.md can be traced and reproduced.
"""
from __future__ import annotations

import argparse
import pathlib
import re
import statistics
from datetime import datetime

ROOT = pathlib.Path(__file__).resolve().parents[2]

DEFAULT_DOS_DIR = ROOT / "logs/experiments/log_driven_validation/20260416_043331/sros2_dos"
DEFAULT_OPEN_MITM_DIR = ROOT / "logs/experiments/03_network_mitm/recorded_20260419_120134"
DEFAULT_SROS2_MITM_DIR = ROOT / "logs/experiments/03_network_mitm/sros2_recorded_20260420_051959"
DEFAULT_GAZEBO_LOG = ROOT / "logs/experiments/03_network_mitm/sros2_gazebo_udp_receiver.log"
DEFAULT_OUT = ROOT / "docs/presentation/data/presentation_metrics.md"


def read_text(path: pathlib.Path) -> str:
    return path.read_text(errors="ignore") if path.exists() else ""


def describe(values: list[float], unit: str = "", decimals: int = 1) -> str:
    if not values:
        return "n/a"
    suffix = f" {unit}" if unit else ""
    return (
        f"count={len(values)} "
        f"min={min(values):.{decimals}f}{suffix} "
        f"avg={statistics.mean(values):.{decimals}f}{suffix} "
        f"max={max(values):.{decimals}f}{suffix}"
    )


def format_stat(stat: dict[str, float] | None, unit: str = "", decimals: int = 1) -> str:
    if not stat:
        return "n/a"
    suffix = f" {unit}" if unit else ""
    return (
        f"count={int(stat['count'])} "
        f"min={stat['min']:.{decimals}f}{suffix} "
        f"avg={stat['avg']:.{decimals}f}{suffix} "
        f"max={stat['max']:.{decimals}f}{suffix}"
    )


def summarize_dos(dos_dir: pathlib.Path) -> dict[str, object]:
    attack = read_text(dos_dir / "dos_attack.log")
    resource = read_text(dos_dir / "resource_monitor.log")
    controller = read_text(dos_dir / "controller_session.log")
    test = read_text(dos_dir / "test.log")

    final = re.search(r"Finished\. Sent (\d+) packets in ([0-9.]+)s, avg (\d+) pkt/s", attack)
    target_ports = re.search(r"Target ports:\s+\[([^\]]+)\]", attack)
    duration = re.search(r"Duration:\s+([0-9.]+)s", attack)
    threads = re.search(r"Threads per port:\s+(\d+)", attack)

    cpus = [float(m.group(1)) for m in re.finditer(r"tb3-attacker\s+([0-9.]+)%", resource)]
    watchdogs = [float(m.group(1)) for m in re.finditer(r"No /cmd_vel_in for ([0-9.]+)s", test)]

    pub_re = re.compile(r"\[INFO\] \[([0-9.]+)\] \[controller\]: published=(\d+)")
    pubs = [(float(m.group(1)), int(m.group(2))) for m in pub_re.finditer(controller)]
    intervals = [b[0] - a[0] for a, b in zip(pubs, pubs[1:])]
    max_interval = max(intervals) if intervals else None
    max_pair = None
    if intervals:
        idx = intervals.index(max_interval)
        max_pair = (pubs[idx], pubs[idx + 1])

    return {
        "dir": dos_dir,
        "target_ports": target_ports.group(1).replace(" ", "") if target_ports else "n/a",
        "duration_s": float(duration.group(1)) if duration else None,
        "threads_per_port": int(threads.group(1)) if threads else None,
        "packets": int(final.group(1)) if final else None,
        "elapsed_s": float(final.group(2)) if final else None,
        "avg_pps": int(final.group(3)) if final else None,
        "max_attacker_cpu": max(cpus) if cpus else None,
        "watchdog_count": len(watchdogs),
        "max_watchdog_s": max(watchdogs) if watchdogs else None,
        "watchdogs": watchdogs,
        "max_controller_interval_s": max_interval,
        "max_controller_interval_pair": max_pair,
        "sources": [
            dos_dir / "dos_attack.log",
            dos_dir / "resource_monitor.log",
            dos_dir / "controller_session.log",
            dos_dir / "test.log",
        ],
    }


def parse_summary_stat(summary: str, label: str) -> dict[str, float] | None:
    pattern = re.escape(label) + r": count=([0-9]+) min=([0-9.]+) avg=([0-9.]+) max=([0-9.]+)"
    m = re.search(pattern, summary)
    if not m:
        return None
    return {
        "count": float(m.group(1)),
        "min": float(m.group(2)),
        "avg": float(m.group(3)),
        "max": float(m.group(4)),
    }


def summarize_mitm(mitm_dir: pathlib.Path, subscriber_name: str) -> dict[str, object]:
    # Presentation numbers are taken from summary.md because the live publisher/subscriber
    # logs may be appended after the recorded runner finishes. The summary is the frozen
    # calculation output generated by the recorded runner at experiment completion.
    summary = read_text(mitm_dir / "summary.md")
    qdisc_text = read_text(mitm_dir / "02_during_attacker_qdisc.txt")
    controller_arp = read_text(mitm_dir / "02_during_controller_arp.txt")
    robot_arp = read_text(mitm_dir / "02_during_robot_arp.txt")

    ping_m = re.search(r"## Ping RTT\ncount=([0-9]+) min=([0-9.]+) avg=([0-9.]+) max=([0-9.]+)", summary)
    ping = None
    if ping_m:
        ping = {
            "count": float(ping_m.group(1)),
            "min": float(ping_m.group(2)),
            "avg": float(ping_m.group(3)),
            "max": float(ping_m.group(4)),
        }
    qdisc = " ".join(line.strip() for line in qdisc_text.splitlines() if line.strip().startswith("qdisc"))

    return {
        "dir": mitm_dir,
        "ping_stat": ping,
        "last_latency_stat": parse_summary_stat(summary, "last_latency_ms"),
        "avg_latency_stat": parse_summary_stat(summary, "avg_latency_ms samples"),
        "max_latency_stat": parse_summary_stat(summary, "max_latency_ms samples"),
        "gaps_stat": parse_summary_stat(summary, "max_gap_ms samples"),
        "qdisc": qdisc or "n/a",
        "controller_arp_evidence": [line for line in controller_arp.splitlines() if "172.28." in line],
        "robot_arp_evidence": [line for line in robot_arp.splitlines() if "172.28." in line],
        "sources": [
            mitm_dir / "summary.md",
            mitm_dir / "ping_controller_to_robot.log",
            mitm_dir / subscriber_name,
            mitm_dir / "02_during_controller_arp.txt",
            mitm_dir / "02_during_robot_arp.txt",
            mitm_dir / "02_during_attacker_qdisc.txt",
        ],
    }


def summarize_gazebo(log_path: pathlib.Path, presentation_samples: int = 175) -> dict[str, object]:
    text = read_text(log_path)
    rx = re.compile(
        r"received=(\d+) rate=(\d+)/s .*last_ros_latency_ms=([0-9.]+) "
        r"max_ros_latency_ms=([0-9.]+)"
    )
    samples = []
    rates = []
    last_ros = []
    max_ros = []
    for m in rx.finditer(text):
        samples.append(int(m.group(1)))
        rates.append(int(m.group(2)))
        last_ros.append(float(m.group(3)))
        max_ros.append(float(m.group(4)))
    # The presentation uses the first 175 receiver status samples from the recorded
    # SROS2 Gazebo run. The log file may continue to grow if a tmux demo is left open.
    rows = list(zip(samples, rates, last_ros, max_ros))[:presentation_samples]
    if rows:
        samples, rates, last_ros, max_ros = map(list, zip(*rows))
    watchdogs = re.findall(r"No UDP command for 0\.5s; published zero stop command", text)[:13]
    return {
        "log": log_path,
        "sample_count": len(samples),
        "rate_avg": statistics.mean(rates) if rates else None,
        "rate_min": min(rates) if rates else None,
        "rate_max": max(rates) if rates else None,
        "last_ros_latency": last_ros,
        "max_ros_latency": max(max_ros) if max_ros else None,
        "watchdog_count": len(watchdogs),
        "sources": [log_path],
    }


def pct(value: float | None) -> str:
    return "n/a" if value is None else f"{value:.2f}%"


def num(value: float | int | None, decimals: int = 1) -> str:
    if value is None:
        return "n/a"
    if isinstance(value, int):
        return f"{value:,}"
    return f"{value:,.{decimals}f}"


def source_list(paths: list[pathlib.Path]) -> str:
    return "\n".join(f"- `{p.relative_to(ROOT) if p.is_absolute() and ROOT in p.parents else p}`" for p in paths)


def write_markdown(args: argparse.Namespace) -> str:
    dos = summarize_dos(args.dos_dir)
    open_mitm = summarize_mitm(args.open_mitm_dir, "ros2_robot_subscriber.log")
    sros2_mitm = summarize_mitm(args.sros2_mitm_dir, "sros2_robot_subscriber.log")
    gazebo = summarize_gazebo(args.gazebo_log)

    lines: list[str] = []
    lines.append("# Presentation Metrics Provenance")
    lines.append("")
    lines.append(f"Generated: `{datetime.now().isoformat(timespec='seconds')}`")
    lines.append("")
    lines.append("Regenerate with:")
    lines.append("")
    lines.append("```bash")
    lines.append("python3 scripts/experiments/summarize_presentation_metrics.py")
    lines.append("```")
    lines.append("")

    lines.append("## Slide 10: SROS2 DoS")
    lines.append("")
    lines.append("Raw sources:")
    lines.append(source_list(dos["sources"]))
    lines.append("")
    lines.append("Calculation logic:")
    lines.append("- Packet count, elapsed time, and average pps are parsed from the final `Finished. Sent ...` line in `dos_attack.log`.")
    lines.append("- Max attacker CPU is the maximum `tb3-attacker` CPU percentage in `resource_monitor.log`.")
    lines.append("- Controller stall is the maximum wall-time interval between consecutive `published=` status lines in `controller_session.log`.")
    lines.append("- Relay watchdog values are parsed from `No /cmd_vel_in for ...` lines in `test.log`.")
    lines.append("")
    lines.append("Results:")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("| --- | ---: |")
    lines.append(f"| Target DDS ports | `{dos['target_ports']}` |")
    lines.append(f"| Duration setting | `{num(dos['duration_s'])} s` |")
    lines.append(f"| Threads per port | `{dos['threads_per_port']}` |")
    lines.append(f"| Packets sent | `{num(dos['packets'])}` |")
    lines.append(f"| Elapsed time | `{num(dos['elapsed_s'])} s` |")
    lines.append(f"| Average rate | `{num(dos['avg_pps'], 0)} pkt/s` |")
    lines.append(f"| Max attacker CPU | `{pct(dos['max_attacker_cpu'])}` |")
    lines.append(f"| Max controller status interval | `{num(dos['max_controller_interval_s'])} s` |")
    lines.append(f"| Relay watchdog events | `{dos['watchdog_count']}` |")
    lines.append(f"| Max watchdog missing-input duration | `{num(dos['max_watchdog_s'])} s` |")
    lines.append("")
    pair = dos.get("max_controller_interval_pair")
    if pair:
        (t1, p1), (t2, p2) = pair
        lines.append("Controller stall evidence:")
        lines.append("")
        lines.append("```text")
        lines.append(f"{t1:.3f} published={p1}")
        lines.append(f"{t2:.3f} published={p2}")
        lines.append("```")
        lines.append("")

    def mitm_section(title: str, data: dict[str, object], subscriber_label: str) -> None:
        lines.append(f"## {title}")
        lines.append("")
        lines.append("Raw sources:")
        lines.append(source_list(data["sources"]))
        lines.append("")
        lines.append("Calculation logic:")
        lines.append("- Presentation values are taken from `summary.md`, the frozen summary generated by the recorded runner at experiment completion.")
        lines.append("- The runner calculated ping RTT from `time=... ms` values in `ping_controller_to_robot.log`.")
        lines.append(f"- The runner calculated ROS2 command latency/gap from `{subscriber_label}` status lines.")
        lines.append("- ARP and qdisc evidence are read directly from the recorded `02_during_*` files.")
        lines.append("")
        lines.append("Results:")
        lines.append("")
        lines.append("| Metric | Value |")
        lines.append("| --- | ---: |")
        lines.append(f"| Ping RTT | `{format_stat(data['ping_stat'], 'ms')}` |")
        lines.append(f"| Last command latency | `{format_stat(data['last_latency_stat'], 'ms')}` |")
        lines.append(f"| Max command latency samples | `{format_stat(data['max_latency_stat'], 'ms')}` |")
        lines.append(f"| Max command gap samples | `{format_stat(data['gaps_stat'], 'ms')}` |")
        lines.append(f"| qdisc during attack | `{data['qdisc']}` |")
        lines.append("")
        lines.append("ARP evidence during attack:")
        lines.append("")
        lines.append("```text")
        for line in data["controller_arp_evidence"][:4]:
            lines.append(f"controller: {line}")
        for line in data["robot_arp_evidence"][:4]:
            lines.append(f"robot: {line}")
        lines.append("```")
        lines.append("")

    mitm_section("Slide 12: Open Network MITM", open_mitm, "ros2_robot_subscriber.log")
    mitm_section("Slide 14: SROS2 Network MITM", sros2_mitm, "sros2_robot_subscriber.log")

    lines.append("## Slide 14: SROS2 Gazebo MITM Visual Evidence")
    lines.append("")
    lines.append("Raw sources:")
    lines.append(source_list(gazebo["sources"]))
    lines.append("")
    lines.append("Calculation logic:")
    lines.append("- Command receive rate and ROS command latency are parsed from the first 175 `udp_cmd_vel_receiver` status lines used for the presentation snapshot.")
    lines.append("- Watchdog stop count is the first 13 `No UDP command for 0.5s` warnings in that run.")
    lines.append("- Expected normal rate is the configured MITM controller rate: `MITM_CMD_RATE_HZ=10` in `scripts/demo/sros2_gazebo_mitm_pane.sh`.")
    lines.append("")
    lines.append("Results:")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("| --- | ---: |")
    lines.append(f"| Receiver status samples | `{gazebo['sample_count']}` |")
    lines.append(f"| Average receive rate | `{num(gazebo['rate_avg'], 2)} commands/s` |")
    lines.append(f"| Receive rate min/max | `{gazebo['rate_min']} / {gazebo['rate_max']} commands/s` |")
    lines.append("| Expected normal rate | `10 commands/s` |")
    lines.append(f"| Last ROS latency | `{describe(gazebo['last_ros_latency'], 'ms')}` |")
    lines.append(f"| Maximum ROS command latency | `{num(gazebo['max_ros_latency'])} ms` |")
    lines.append(f"| Watchdog stop events | `{gazebo['watchdog_count']}` |")
    lines.append("")

    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--dos-dir", type=pathlib.Path, default=DEFAULT_DOS_DIR)
    parser.add_argument("--open-mitm-dir", type=pathlib.Path, default=DEFAULT_OPEN_MITM_DIR)
    parser.add_argument("--sros2-mitm-dir", type=pathlib.Path, default=DEFAULT_SROS2_MITM_DIR)
    parser.add_argument("--gazebo-log", type=pathlib.Path, default=DEFAULT_GAZEBO_LOG)
    parser.add_argument("--out", type=pathlib.Path, default=DEFAULT_OUT)
    args = parser.parse_args()

    content = write_markdown(args)
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(content + "\n")
    print(f"Wrote {args.out}")


if __name__ == "__main__":
    main()
